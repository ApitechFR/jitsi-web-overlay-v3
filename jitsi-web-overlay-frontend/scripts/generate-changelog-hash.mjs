#!/usr/bin/env node
import fs from 'fs';
import path from 'path';
import crypto from 'crypto';
import { fileURLToPath } from 'url';

// Get current directory for ES modules
const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

// Path to infos.json file
const changelogPath = path.join(__dirname, '../public/infos.json');

// Path to output TypeScript file
const outputPath = path.join(__dirname, '../src/utils/changelogs/changelog-version.ts');

try {
    // Read infos.json file content
    const fileContent = fs.readFileSync(changelogPath, 'utf-8');

    // Generate MD5 hash of the content
    const hash = crypto.createHash('md5').update(fileContent).digest('hex').substring(0, 8);

    // Ensure output directory exists
    const outputDir = path.dirname(outputPath);
    if (!fs.existsSync(outputDir)) {
        fs.mkdirSync(outputDir, { recursive: true });
    }

    // Generate TypeScript file content
    const tsContent = `// Auto-generated file - do not edit manually
// Generated by scripts/generate-changelog-hash.mjs
export const CHANGELOG_VERSION = '${hash}';
`;

    // Write the TypeScript file
    fs.writeFileSync(outputPath, tsContent, 'utf-8');
    console.log(` Hash generated: ${hash}`);
    console.log(` File created: ${outputPath}`);

    // Copy infos.json to infos-{hash}.json for cache busting
    const hashedChangelogPath = path.join(__dirname, `../public/infos-${hash}.json`);
    fs.copyFileSync(changelogPath, hashedChangelogPath);
    console.log(` Versioned changelog created: ${hashedChangelogPath}`);
} catch (error) {
    console.error(' Error generating hash:', error.message);
    process.exit(1);
}
